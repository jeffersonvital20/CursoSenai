import java.util.Scanner;

public class Aula_21_ParaEnquanto_01 {
    public static void main(String[] args){

        for (int i = 150; i <= 300; i++) {
            System.out.print(i+", ");
        }
        System.out.println("");
        int cont =150;
        while (cont<=300){
            System.out.print(cont + ", ");
            cont++;
        }
    }

}
